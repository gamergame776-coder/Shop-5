// Current language (default: Thai)
let currentLanguage = localStorage.getItem('language') || 'th';

// Get translation
function t(key) {
    return translations[currentLanguage][key] || key;
}

// Change language
function changeLanguage(lang) {
    if (!translations[lang]) return;
    
    currentLanguage = lang;
    localStorage.setItem('language', lang);
    
    // Update all translated elements
    updateTranslations();
    
    // Update language selector UI
    updateLanguageUI();
    
    // Show notification
    const message = lang === 'th' ? '‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡∏†‡∏≤‡∏©‡∏≤‡πÄ‡∏õ‡πá‡∏ô‡πÑ‡∏ó‡∏¢‡πÅ‡∏•‡πâ‡∏ß' : 'Changed to English';
    if (typeof showNotification === 'function') {
        showNotification(message, 'success', t('success'));
    }
    
    // Play sound if enabled
    if (typeof userSettings !== 'undefined' && userSettings.soundEffects && typeof playSound === 'function') {
        playSound('click');
    }
}

// Update all translations on page
function updateTranslations() {
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translation = t(key);
        
        // Check if translation contains HTML
        if (translation.includes('<br>')) {
            element.innerHTML = translation;
        } else {
            element.textContent = translation;
        }
    });
    
    // Update placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        element.placeholder = t(key);
    });
    
    // Update title attributes
    document.querySelectorAll('[data-i18n-title]').forEach(element => {
        const key = element.getAttribute('data-i18n-title');
        element.title = t(key);
    });
}

// Update language selector UI
function updateLanguageUI() {
    // Update language buttons
    document.querySelectorAll('.language-option').forEach(option => {
        option.classList.remove('active');
        if (option.dataset.lang === currentLanguage) {
            option.classList.add('active');
        }
    });
    
    // Update language toggle button
    const langToggle = document.getElementById('languageToggle');
    if (langToggle) {
        langToggle.textContent = currentLanguage === 'th' ? 'üáπüá≠ TH' : 'üá∫üá∏ EN';
    }
}

// Initialize translations on page load
function initializeTranslations() {
    updateTranslations();
    updateLanguageUI();
    console.log('üåç Translations initialized:', currentLanguage);
}

// Auto-initialize when DOM is ready
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initializeTranslations);
} else {
    initializeTranslations();
}